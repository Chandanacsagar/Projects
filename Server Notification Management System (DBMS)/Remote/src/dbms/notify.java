    private void InsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InsertActionPerformed
        // TODO add your handling code here:
         jPanel2.setVisible(true);
  
        try {
            // TODO add your handling code here
            String NotifID,Password;

            //Org_ID = n1.getText();
            NotifID = n2.getText();
            Password = n3.getText();
            //Status= (String) O5.getSelectedItem();
            Statement stmt=con.createStatement();
            ResultSet res=stmt.executeQuery("select * from notification_team where Org_ID="+Integer.parseInt(n1.getSelectedItem().toString()));
            if(res.next())
            {
                 JOptionPane.showMessageDialog(this, "Notify id already exists for this organization");
                 return;
            }
            pst = con.prepareStatement("insert into notification_team(Org_ID,NotifID,Password)values(?,?,?)");
            pst.setInt(1, Integer.parseInt(n1.getSelectedItem().toString()));
            pst.setString(2,NotifID);
            pst.setString(3, Password);           
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Record Saved");
            
            table_update();

            n2.setText("");
            n3.setText("");
            n1.requestFocus();

        } catch (SQLException ex) {
            ex.printStackTrace();
        }  
    }//GEN-LAST:event_InsertActionPerformed

