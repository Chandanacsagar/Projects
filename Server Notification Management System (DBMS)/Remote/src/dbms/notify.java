    private void table_update() {
        int CC;
        try {
            pst = con.prepareStatement("SELECT * FROM notification_team");
            ResultSet Rs = pst.executeQuery();

            ResultSetMetaData RSMD = Rs.getMetaData();
            CC = RSMD.getColumnCount();
            DefaultTableModel DFT = (DefaultTableModel) notde.getModel();
            DFT.setRowCount(0);

            while (Rs.next()) {
                Vector v2 = new Vector();
                for (int i = 1; i <= CC; i++) {
                    v2.add(Rs.getInt("Org_ID"));
                    v2.add(Rs.getString("NotifID"));
                    v2.add(Rs.getString("Password"));
                    
                }
                DFT.addRow(v2);
            }
        } catch (Exception e) {

        }
    }

